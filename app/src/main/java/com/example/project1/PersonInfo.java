package com.example.project1;

public class PersonInfo {
    public String name;
    public String num;
    public String photo;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }
    public void setPhoto(String photo) {
        this.photo = photo;
    }
}
